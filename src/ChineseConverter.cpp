static std::unordered_map<std::string_view, std::string_view>*
CreateLookupTable();

static std::string Lookup(std::string text) {
  static const std::unordered_map<std::string_view, std::string_view>* m =
      CreateLookupTable();

  auto it = m->find(text);
  if (it != m->end()) {
    return std::string(it->second);
  }
  return text;
}

std::string ConvertToSimplifiedChinese(const std::string& text) {
  std::u32string u32s = ToU32(text);
  std::string converted;
  for (const char32_t u32char : u32s) {
    converted += Lookup(ToU8(std::u32string(&u32char, 1)));
  }
  return converted;
}

